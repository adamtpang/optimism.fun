export const getCompaniesForProblem = (problemSlug: string): Company[] =>
  companies
    .filter((c) => c.problemSlugs.includes(problemSlug))
    .sort((a, b) => {
      const capA = a.marketCap?.value ?? a.valuation?.value ?? 0
      const capB = b.marketCap?.value ?? b.valuation?.value ?? 0
      return capB - capA
    })

export const getCompanyBySlug = (slug: string): Company | undefined =>
  companies.find((c) => c.slug === slug)
